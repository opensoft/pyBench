#!/usr/bin/env bash
# Generate a Docker Compose override for WSL AMD ROCm exposure when this host
# provides the required WSL GPU device and ROCm runtime libraries.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BENCH_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
OUT_FILE="${BENCH_DIR}/.devcontainer/docker-compose.amd-rocm.generated.yml"
ROCM_ROOT="${ROCM_ROOT:-/opt/rocm-7.2.0}"

has_amd_rocm=1
[[ -e /dev/dxg ]] || has_amd_rocm=0
[[ -r "${ROCM_ROOT}/lib/libamdhip64.so.7" ]] || has_amd_rocm=0
[[ -r "${ROCM_ROOT}/lib/libhsa-runtime64.so.1" ]] || has_amd_rocm=0

mkdir -p "$(dirname "$OUT_FILE")"

if [[ "$has_amd_rocm" != "1" ]]; then
  cat >"$OUT_FILE" <<'YAML'
# Generated by scripts/configure-amd-rocm-wsl.sh.
# No WSL AMD ROCm runtime was detected on the host; this override is inert.
services:
  py-bench: {}
YAML
  echo "pyBench AMD ROCm: not detected; wrote inert override ${OUT_FILE}"
  exit 0
fi

cat >"$OUT_FILE" <<YAML
# Generated by scripts/configure-amd-rocm-wsl.sh.
# Host WSL AMD ROCm runtime detected; recreate py-bench after this file changes.
services:
  py-bench:
    devices:
      - /dev/dxg:/dev/dxg
    volumes:
      - ${ROCM_ROOT}:${ROCM_ROOT}:ro
YAML

declare -A wsl_libs=(
  [/usr/lib/wsl/lib/libdxcore.so]=/usr/lib/libdxcore.so
  [/usr/lib/wsl/lib/libd3d12.so]=/usr/lib/libd3d12.so
  [/usr/lib/wsl/lib/libd3d12core.so]=/usr/lib/libd3d12core.so
)

for src in "${!wsl_libs[@]}"; do
  if [[ -r "$src" ]]; then
    printf '      - %s:%s:ro\n' "$src" "${wsl_libs[$src]}" >>"$OUT_FILE"
  fi
done

cat >>"$OUT_FILE" <<YAML
    environment:
      ROCM_PATH: ${ROCM_ROOT}
      HIP_PATH: ${ROCM_ROOT}
      HSA_ENABLE_DXG_DETECTION: "1"
      HSA_ENABLE_SDMA: "0"
      LD_PRELOAD: ${ROCM_ROOT}/lib/libhsa-runtime64.so.1
      LD_LIBRARY_PATH: ${ROCM_ROOT}/lib:/usr/lib/x86_64-linux-gnu:/usr/lib
YAML

echo "pyBench AMD ROCm: detected; wrote ${OUT_FILE}"
